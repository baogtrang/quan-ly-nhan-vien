// object class js
function Employee (_id, _name, _email, _password, _workDate, _basicSalary, _role, _workHours, _totalSalary, _category ){
    this.id = _id;
    this.name = _name;
    this.email = _email;
    this.password = _password;
    this.workDate = _workDate;
    this.basicSalary = _basicSalary;
    this.role = _role;
    this.workHours = _workHours;
    this.totalSalary = _totalSalary;
    this.category = _category;
}